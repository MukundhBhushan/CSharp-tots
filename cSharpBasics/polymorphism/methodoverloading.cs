using System;

namespace cSharpBasic
{
    class baseClass
    {
        public int add(int a,int b)
        {
            return a+b;
        }
        public int add(int a, int b, int c)
        {
            return a+b+c;
        }
        public string add(string a, string b)
        {
            return a+b;
        }
        public float add(float a, int b)
        {
            return a+b;
        }
            
    }
